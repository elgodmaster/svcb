<h1>Bienvenido a SVCB!</h1>
<p>Usted como Administrador del sistema podrá realizar las siguientes tareas:</p>
<p>Gestionar los usuarios del sistema según necesite, ya sea crear, buscar, modificar o eliminar un usuario.</p>
<p>Gestionar los clientes con los que trabaja su empresa según sea necesario, ingresando nuevos productos, buscando información de alguno existente, modificando o eliminando algún cliente del sistema.</p>
<p>Gestionar los productos del sistema, tanto sus categorías como los productos en concreto. Podrá crear, buscar, modificar y eliminar categorías y productos según necesite.</p>
<p>Por último pero no menos importante, dispone de una sección de ayuda en donde encontrará toda la información relacionada con el funcionamiento del sistema. Para facilitar el uso de este, es que se pone a disposición de usted un video tutorial con las diferentes funcionalidades del sistema de acuerdo a sus privilegios de usuario dentro de este, un manual de usuario e información del software, como su versión y tecnologías usadas para el desarrollo de este.
</p>
