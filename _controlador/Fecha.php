<?php
 
 function nombreMes($nombre)
 {
	 if ($nombre == 'enero')
	 	 return $mes='01';
	 elseif($nombre == 'febrero')
	 	 return $mes='02';
	 elseif ($nombre == 'marzo')
	 	 return $mes='03';
	 elseif ($nombre == 'abril')
	 	 return $mes='04';
	 elseif ($nombre == 'mayo')
	 	 return $mes='05';
	 elseif ($nombre == 'junio')
	 	 return $mes='06';
	 elseif ($nombre == 'julio')
	 	 return $mes='07';
	 elseif ($nombre == 'agosto')
	 	 return $mes='08';
	 elseif ($nombre == 'septiembre')
	 	 return $mes='09';
	 elseif ($nombre == 'octubre')
	 	 return $mes='10';
	 elseif ($nombre == 'noviembre')
	 	 return $mes='11';
	 elseif($nombre == 'diciembre')
	 	 return $mes='12';
 }

?>