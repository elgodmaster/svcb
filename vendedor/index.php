	<h1>Bienvenido a SVCB!</h1>
	<p>Usted como Vendedor del sistema podrá realizar las siguientes tareas:</p>
	<ul>
		<li type="square">Gestionar los clientes con los que trabaja su empresa según sea necesario, ingresando nuevos clientes, buscando información de alguno existente, modificando o eliminando alguno del sistema.</li>
		<li type="square">Realizar venta a un determinado cliente.</li>
		<li type="square">Relizar un cobro a un determinado cliente.</li>
		<li type="square">Relizar reportes por cliente y/o por clientes morosos, mostrando en detalle las ventas realizadas y el estado actual de éstas.</li>
	</ul>
	<p>Por último pero no menos importante, dispone de una sección de ayuda en donde encontrará toda la información relacionada con el funcionamiento del sistema. Para facilitar el uso de éste, es que se pone a disposición de usted un video tutorial con las diferentes funcionalidades del sistema de acuerdo a sus privilegios de usuario dentro de éste, un manual de usuario e información del software, como su versión y tecnologías usadas para el desarrollo de éste.
	</p>
